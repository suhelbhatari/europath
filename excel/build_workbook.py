"""
EuroPath Immigration Explorer — Excel Workbook Builder
Generates a comprehensive 12-sheet workbook with dashboard, live country
search, comparisons, full pathway detail, charts, and conditional formatting.

Usage:
    pip install openpyxl
    python build_workbook.py

Input:  countries.json (45 countries, generated by scripts/export-countries.js)
Output: EuroPath_Immigration_Explorer.xlsx
"""
import json
import os
from openpyxl import Workbook
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
from openpyxl.formatting.rule import ColorScaleRule, FormulaRule
