with open(os.path.join(HERE, "countries.json")) as f:
    COUNTRIES = json.load(f)

# ─── COLOR PALETTE ──────────────────────────────────────────
NAVY, DARK = "0F1A2E", "131F35"
PRIMARY, GREEN, BLUE = "6366F1", "22C55E", "0EA5E9"
AMBER, RED, PURPLE = "F59E0B", "EF4444", "A855F7"
WHITE, LIGHTGRAY, MIDGRAY, DARKTEXT = "FFFFFF", "F1F5F9", "94A3B8", "1E293B"
FONT_NAME = "Calibri"

def header_font(size=11, color=WHITE): return Font(name=FONT_NAME, bold=True, size=size, color=color)
def title_font(size=18, color=DARKTEXT): return Font(name=FONT_NAME, bold=True, size=size, color=color)
def body_font(size=10, color=DARKTEXT, bold=False, italic=False):
    return Font(name=FONT_NAME, size=size, color=color, bold=bold, italic=italic)
def fill(color): return PatternFill("solid", fgColor=color)

THIN = Side(style="thin", color="CBD5E1")
BORDER_ALL = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)

def style_header_row(ws, row, n_cols, color=PRIMARY, start_col=1, size=10):
    for c in range(start_col, start_col + n_cols):
        cell = ws.cell(row=row, column=c)
        cell.font = header_font(size=size)
        cell.fill = fill(color)
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        cell.border = BORDER_ALL

def autosize(ws, widths):
    for col, w in widths.items():
        ws.column_dimensions[col].width = w

def freeze(ws, cell="A2"):
    ws.freeze_panes = cell

